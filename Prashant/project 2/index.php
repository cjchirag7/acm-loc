      <h2 id="about">About</h2>
      <p>The Central Library of Indian Institute of Technology (Indian School of Mines) Dhanbad is the heart of the institute providing direct academic and research supports to all departments.</p>

<p>The Central Library is an automated library in terms of records organisation and management of all its different sections, search and discovery, information retrieval and service delivery. The whole library operations run on LIBSYS integrated library management software. Over the years the library has developed several databases of books, journals, theses/ dissertations, conference proceedings, etc. All documents have been bar-coded. Focus has been given on collection build-up of print resources as well as licensed e-resources plus E-Shodh Sindhu sponsored resources. The library has reached all the departments, hostels, and quarters on the campus wide LAN.</p>

<p>The library has initiated the development of the “Institutional Digital Repository” based on DSpace open source platform. The Central Library has set up its “Research Documentation Cell” based on the research outputs which are indexed in Scopus and Web of Science.</p>

<p>To meet the challenges of increasing number of students, research scholars and faculty members as well as ever changing information and communication technologies a G+7 centrally air-conditioned library having state-of-the-art facilities is being developed to provide top quality library services.</p>

<p>The staff of the library strive their best to help push the boundaries of IIT (ISM) Dhanbad in national and international spaces.</p>
