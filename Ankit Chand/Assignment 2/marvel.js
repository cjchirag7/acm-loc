(function() 
 {
  var allQuestions = [{
    question: "What was Red Skull's real name?",
    options: ["Johann Schmidt", "Steve Rogers", "Jhony Storm", "Jack Hammer"],
    answer: 2
  }, {
    question: "In 'The Avengers,' what S.H.I.E.L.D. agent does Black Widow say first brought her into the fold?",
    options: ["Hawkeye", "Agent Coulson", "Peggy Carter", "Nick Fury"],
    answer: 0
  }, {
    question: "Who played Nebula in 'Guardians of the Galaxy'?",
    options: ["Kat Dennings", "Karen Gillan", "Zoe Saldana","Jaimie Alexander"],
    answer: 1
  },{
    question: "Peter Parker works as a photographer for:",
    options: ["The Daily Planet", "The Daily Bugle", "The New York Time", "The Rolling Stone"],
    answer: 1
  }, {
    question: "S.H.I.E.L.D.'s highest ranking agent is: ?",
    options: ["Nick Fury", "Steve Rogers", "Peter Parker", "Natalia Romanova"],
    answer: 0
  }];
  
  
  var quesCounter = 0;
  var selectOptions = [];
  var quizSpace = $('#quiz');
    
  nextQuestion();
    
  $('#next').click(function () 
    {
        chooseOption();
        if (isNaN(selectOptions[quesCounter])) 
        {
          alert('You didn\'t selected any option !');
          quesCounter++;
          nextQuestion();
        } 
        else 
        {
          quesCounter++;
          nextQuestion();
        }
    });
  
  $('#prev').click(function () 
    {
        chooseOption();
        quesCounter--;
        nextQuestion();
    });
  
  function createElement(index) 
    {
        var element = $('<div>',{id: 'question'});
        var header = $('<h2>Question No. ' + (index + 1) + ' :</h2>');
        element.append(header);

        var question = $('<p>').append(allQuestions[index].question);
        element.append(question);

        var radio = radioButtons(index);
        element.append(radio);

        return element;
    }
  
  function radioButtons(index) 
    {
        var radioItems = $('<ul>');
        var item;
        var input = '';
        for (var i = 0; i < allQuestions[index].options.length; i++) {
          item = $('<li>');
          input = '<input type="radio" name="answer" value=' + i + ' />';
          input += allQuestions[index].options[i];
          item.append(input);
          radioItems.append(item);
        }
        return radioItems;
  }
  
  function chooseOption() 
    {
        selectOptions[quesCounter] = +$('input[name="answer"]:checked').val();
    }
   
  function nextQuestion() 
    {
        quizSpace.fadeOut(function() 
            {
              $('#question').remove();
              if(quesCounter < allQuestions.length)
                {
                    var nextQuestion = createElement(quesCounter);
                    quizSpace.append(nextQuestion).fadeIn();
                    if (!(isNaN(selectOptions[quesCounter]))) 
                    {
                      $('input[value='+selectOptions[quesCounter]+']').prop('checked', true);
                    }
                    if(quesCounter === 1)
                    {
                      $('#prev').show();
                    } 
                    else if(quesCounter === 0)
                    {
                      $('#prev').hide();
                      $('#next').show();
                    }
                }
              else 
                {
                    var scoreRslt = displayResult();
                    quizSpace.append(scoreRslt).fadeIn();
                    $('#next').hide();
                    $('#prev').hide();
                }
        });
    }
   
  
  function displayResult() 
    {
        var score = $('<p>',{id: 'question'});
        var correct = 0;
        for (var i = 0; i < selectOptions.length; i++) 
        {
          if (selectOptions[i] === allQuestions[i].answer) 
          {
            correct++;
          }
        }
        score.append('You scored ' + correct + ' out of ' +allQuestions.length);
        return score;
  }
})();


